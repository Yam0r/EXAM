const burgerMenu = document.querySelector('#burger');
const navMenu = document.querySelector('#under_header');

burgerMenu.addEventListener('click', () => {
  navMenu.classList.toggle('active');
});

$("#btn-1").on("click", function () {
  $("#div-1").slideToggle();
});
$(function () {
  $("#btn-1").on("click", function () {
    if ($('p',this).hasClass("arrow")) {
      $('p',this).removeClass("arrow").addClass("arrow-rotate");
    } else if ($('p',this).hasClass("arrow-rotate")) {
      $('p',this).removeClass("arrow-rotate").addClass("arrow");
    }
  });
});



